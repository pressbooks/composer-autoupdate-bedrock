use League\Csv\Reader;

/**
 * Replace core BISAC Subject(s) field.
 *
 * @since 2.0.0
 *
 * @param array $args
 * @return array
 */
function bisac_subjects_field_arguments( $args ) {
	$csv                = file_get_contents( PB_BISAC_PLUGIN_DIR . 'data/bisac.csv' );
	$reader             = Reader::createFromString( $csv );

	$codes = [];

	foreach ( $reader as $key => $value ) {
		if ( $key === 0 ) {
			continue;
		}

		$codes[ $value[0] ] = $value[1];
	}

	$args['field_type'] = 'multi_select';
	$args['multiple']   = false;
	$args['values']     = $codes;
	$args['select2']    = true;

	return $args;
}

